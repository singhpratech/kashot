pub fn glyph(c: char) -> &'static [u8; 7] {
    let code = c as u32;
    if (0x20..=0x7E).contains(&code) {
        &FONT_5X7[(code - 0x20) as usize]
    } else {
        // Fallback to '?' for non-printable / non-ASCII input.
        &FONT_5X7[(0x3F - 0x20) as usize]
    }
}

/// Width of a string in pixels at the given scale (5px-per-glyph + 1px gap).
pub fn measure(text: &str, scale: i32) -> i32 {
    if text.is_empty() { return 0; }
    let n = text.chars().count() as i32;
    n * 5 * scale + (n - 1) * scale
}

